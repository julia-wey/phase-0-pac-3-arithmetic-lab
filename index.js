function add(a, b) {
    return a + b;
}
function subtract(a, b) {
    return a - b
}
function multiply(a, b) {
    return a * b
}
function divide(a, b) {
    return a / b
}
let n = 5;
function increment(a){
    n = a + 1;
    return n;
}
function decrement(a){
    n = a - 1;
    return n;
}
function makeInt(a){
    n = parseInt(a, 10);
    return n;
}
function preserveDecimal(a){
    n = parseFloat(a, 10);
    return n;
}
